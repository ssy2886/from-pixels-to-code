                new SourceBlock("Sensor", "PressureSensor",
                    new List<string> { "RawValue" },
                    new List<string> { "Pressure", "Alarm" })
            };

            var exampleStBlocks = new List<STFunBlockInfo>
            {
                new STFunBlockInfo
                {
                    BlockName = "Valve",
                    FunctionName = "ControlValve_FB",
                    Inputs = new List<string> { "InputSignal", "Setpoint" },
                    Outputs = new List<string> { "Position", "Status" },
                    Code = new List<string>
                    {
                        "Position := InputSignal * Setpoint;",
                        "Status := Position > 0.5;"
                    }
                },
                new STFunBlockInfo
                {
                    BlockName = "Pump",
                    FunctionName = "Pump_FB",
                    Inputs = new List<string> { "StartCmd", "SpeedSet" },
                    Outputs = new List<string> { "Speed", "Running" },
                    Code = new List<string>
                    {
                        "IF StartCmd THEN",
                        "    Speed := SpeedSet;",
                        "    Running := TRUE;",
                        "ELSE",
                        "    Speed := 0.0;",
                        "    Running := FALSE;",
                        "END_IF;"
                    }
                }
            };

            var result = GenerateFromEngineeringData(
                exampleBlocks,
                exampleStBlocks,
                new Dictionary<ObjectId, LineStruct>(),
                new Dictionary<ObjectId, BlockStruct>());

            return result.GeneratedCode;
        }
